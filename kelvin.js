const kelvin = 0; // this is the kelvin variable
const celsius = kelvin - 273; // convert
let fahrenheit = celsius * (9/5) + 32;

fahrenheit = Math.floor(fahrenheit); // convert
console.log('The temperature is ' + fahrenheit + ' degrees Fahrenheit.');

